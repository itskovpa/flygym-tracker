@pytest.mark.parametrize("n_spans", [7, 9])
def test_builder_rejects_a_span_count_that_is_not_eight(n_spans):
    """Anything but 8 columns would break the global-vial-id scheme SILENTLY.

    Ids here run 1..2*len(spans) while `pipeline.TrackerPipeline` computes global ids as
    `face_index*16 + local_id`. With 9 columns, face A's local id 17 and face B's local id 1
    both map to global id 17 -- two physical vials merged into one row of the output table,
    with nothing reporting it. So this must fail loudly at calibration time.
    """
    spans = [(60 + i * 70, 60 + i * 70 + 61) for i in range(n_spans)]
    with pytest.raises(ValueError, match="expected exactly 8"):
        build_calibration_from_markers(make_face_frame(), "A", StubDetector(spans=spans))


def test_box_w_frac_shrinks_boxes_about_the_span_centre():
    params = MarkerCalibParams(box_w_frac=0.5)
    face_cal, _, _ = build_calibration_from_markers(
        make_face_frame(), "A", StubDetector(), params=params)
    for v, (sx0, sx1) in zip(face_cal.vials[:8], TRUE_SPANS):
        assert v.w == (sx1 - sx0 + 1) // 2
        span_mid = (sx0 + sx1 + 1) / 2.0
        assert abs((v.x + v.w / 2.0) - span_mid) <= 1.0


