    assert det.identify_face(make_face_frame(swap=True)) == "B"

    # ... and a detector rebuilt from the saved bundle alone behaves the same
    reloaded = marker_detector_from_calibration(load_calibration(out))
    assert reloaded is not None
    assert reloaded.identify_face(make_face_frame(swap=False)) == "A"
    assert reloaded.identify_face(make_face_frame(swap=True)) == "B"


