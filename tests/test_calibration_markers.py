def write_flip_video(path: str, blocks=("A", "B", "A", "B"), dwell_frames: int = 20,
                     rotation_frames: int = 4) -> int:
    """Write a synthetic flip clip: dwell / rotation / dwell / ... Returns the frame count.

    Uncompressed AVI (fourcc 0) so the marker pattern survives byte-exact. Dwell frames carry a
    seeded +-1 dither so the frame-to-frame metric is non-zero as a real sensor's is; rotation
    frames are the face rolled sideways, which is a large global change exactly as a turning drum
    produces.
    """
    writer = cv2.VideoWriter(path, 0, 10.0, (W, H), False)
    assert writer.isOpened(), "could not open an uncompressed AVI writer"
    n = 0
    try:
        for b, face in enumerate(blocks):
            base = make_face_frame(swap=(face == "B"))
            rng = np.random.default_rng(100 + b)
            for _ in range(dwell_frames):
                dither = rng.integers(-1, 2, size=base.shape)
                writer.write(np.clip(base.astype(np.int16) + dither, 0, 255).astype(np.uint8))
                n += 1
            if b < len(blocks) - 1:
                for k in range(rotation_frames):
                    writer.write(np.roll(base, 90 * (k + 1), axis=1))
                    n += 1
    finally:
        writer.release()
    return n


