def test_upper_ids_sit_in_the_upper_band_and_lower_ids_below():
    face_cal, _, _ = build_calibration_from_markers(make_face_frame(), "A", StubDetector())
    for v in face_cal.vials[:8]:
        assert_band_close((v.y, v.y + v.h), UPPER_BAND)
    for v in face_cal.vials[8:]:
        assert_band_close((v.y, v.y + v.h), LOWER_BAND)
    # every box in a row shares one y extent ...
    assert len({(v.y, v.h) for v in face_cal.vials[:8]}) == 1
    assert len({(v.y, v.h) for v in face_cal.vials[8:]}) == 1
    # ... and the marker band lies strictly between the two rows, in neither of them
    top, bottom = face_cal.vials[0], face_cal.vials[8]
    assert top.y + top.h <= STRIP_UPPER[0]
    assert bottom.y > STRIP_LOWER[1]


