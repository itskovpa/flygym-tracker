def _two_face_calibration():
    det = MarkerBandDetector()
    det.register_face(make_face_frame(swap=False), "A")
    det.register_face(make_face_frame(swap=True), "B")
    fa, ma, oa = build_calibration_from_markers(make_face_frame(swap=False), "A", det)
    fb, mb, ob = build_calibration_from_markers(make_face_frame(swap=True, dim_columns=(7,)), "B", det)
    calib = Calibration(image_width=W, image_height=H, faces={"A": fa, "B": fb}, notes="test")
    return calib, {"A": ma, "B": mb}, {"A": oa, "B": ob}


def test_save_load_roundtrip_two_faces(tmp_path):
    calib, masks, overlays = _two_face_calibration()
    out = str(tmp_path / "calib")
    save_calibration(calib, masks, out, overlay=overlays)

    for name in ("calibration.json", "illum_mask_A.png", "illum_mask_B.png",
                 "overlay_A.png", "overlay_B.png"):
        assert os.path.isfile(os.path.join(out, name)), name

    loaded = load_calibration(out)
    assert sorted(loaded.faces) == ["A", "B"]
    assert (loaded.image_width, loaded.image_height) == (W, H)
    for name in ("A", "B"):
        orig = calib.faces[name].vials
        got = loaded.faces[name].vials
        assert len(orig) == len(got) == 16
        for a, b in zip(orig, got):
            assert (a.id, a.row, a.col, a.x, a.y, a.w, a.h, a.present) == \
                   (b.id, b.row, b.col, b.x, b.y, b.w, b.h, b.present)
        assert all(v.present for v in got), "presence policy did not survive the round-trip"
        assert suspicious_vials(loaded.faces[name]) == suspicious_vials(calib.faces[name])

    # each face got its OWN mask, and both exclude the central band
    ma = cv2.imread(loaded.faces["A"].illum_mask_path, cv2.IMREAD_GRAYSCALE)
    mb = cv2.imread(loaded.faces["B"].illum_mask_path, cv2.IMREAD_GRAYSCALE)
    assert ma is not None and mb is not None
    assert int(ma[TRUE_CENTRAL[0]:TRUE_CENTRAL[1]].max()) == 0
    assert int(mb[TRUE_CENTRAL[0]:TRUE_CENTRAL[1]].max()) == 0
    assert not np.array_equal(ma, mb), "both faces were written the same mask"


