"""Synthetic, deterministic tests for the MARKER-DRIVEN calibration path.

Everything here is generated by `make_face_frame` -- no video file from the rig, no randomness
that matters (the only RNG is a seeded +-1 grey-level dither, needed so a synthetic "dwell" has a
non-zero frame-to-frame metric like a real sensor does).

The synthetic frame reproduces the parts of the real rig that the marker calibration path
actually depends on (see `calibration`'s module docstring and `marker_band`'s):

  * two glowing TUBE ROW-BANDS, one above and one below the marker band, each spanning 8 vial
    columns -- these carry the y extents of the 16 slots;
  * the central MARKER BAND: two thin bright strips carrying one opaque sticker per vial column,
    alternating upper/lower, which is what encodes both the vial x boundaries and the face id;
  * a large SATURATED STAGE along the bottom edge that is BRIGHTER than either tube row and tall
    enough to qualify as a row-band candidate. On the real rig this is the illuminated stage
    (measured rows ~893-1023 at mean ~228, versus the lower tube row at ~127), so any
    "brightest/biggest run wins" rule picks it instead of a tube row. Keeping it here pins the
    rule that actually works: pick the run NEAREST THE MARKER BAND on each side.

Face "A" is the pattern as built; face "B" is the same pattern with the two strips swapped, which
is what a 180 degree drum flip produces.
"""
