# --- synthetic rig geometry (proportioned like the real 1280x1024 frame, scaled down) ---------
H, W = 760, 800
BG = 20                       # unlit surround
HARDWARE = 12                 # dark mounting hardware around/behind the marker strips
UPPER_BAND = (60, 292)        # HALF-OPEN rows of the upper tube row
LOWER_BAND = (410, 642)       # HALF-OPEN rows of the lower tube row
STRIP_UPPER = (320, 340)      # INCLUSIVE rows of the upper marker strip
STRIP_LOWER = (360, 380)      # INCLUSIVE rows of the lower marker strip
STAGE_ROW = 670               # saturated stage occupies [STAGE_ROW, H): brighter than the tubes
TUBE_CORE = 150               # bright tube body
TUBE_EDGE = 85                # dimmer rows at each band's outer edge (the tube mouths)
EDGE_ROWS = 24
TUBE_DIM = 45                 # a slot whose column barely lights: SUSPICIOUS, never auto-excluded
STRIP_LIT = 250
STAGE_VAL = 255

N_VIALS = 8
VIAL_X0 = 80
VIAL_PITCH = 80
#: ground-truth INCLUSIVE (x0, x1) of every vial column -- what `vial_boundaries` must return.
TRUE_SPANS = [(VIAL_X0 + i * VIAL_PITCH, VIAL_X0 + (i + 1) * VIAL_PITCH - 1) for i in range(N_VIALS)]
X_EXTENT = (TRUE_SPANS[0][0], TRUE_SPANS[-1][1])
#: the region between the two tube rows -- marker strips + hardware -- excluded from the mask.
TRUE_CENTRAL = (UPPER_BAND[1], LOWER_BAND[0])


def make_face_frame(swap: bool = False, dim_columns=(), dither: int = 0) -> np.ndarray:
    """Build a synthetic drum-face frame.

    Args:
        swap: emit the 180-degree-flipped face (upper/lower strip contents exchanged).
        dim_columns: 0-based vial columns whose tube barely lights, in BOTH rows. These are the
            real rig's dim end columns (DESIGN.md §2) -- the ones the brightness-based detector
            wrongly called empty.
        dither: peak amplitude of a seeded +-`dither` grey-level dither, so consecutive frames of
            a synthetic "dwell" differ slightly, as a real sensor's do.
    """
    img = np.full((H, W), BG, dtype=np.uint8)
    img[STAGE_ROW:, :] = STAGE_VAL
    img[TRUE_CENTRAL[0]:TRUE_CENTRAL[1], :] = HARDWARE

    x0, x1 = X_EXTENT
    for y0, y1 in (UPPER_BAND, LOWER_BAND):
        img[y0:y1, x0:x1 + 1] = TUBE_EDGE
        img[y0 + EDGE_ROWS:y1 - EDGE_ROWS, x0:x1 + 1] = TUBE_CORE
        for c in dim_columns:
            sx0, sx1 = TRUE_SPANS[c]
            img[y0:y1, sx0:sx1 + 1] = TUBE_DIM

    # Marker band: one opaque sticker per column, alternating upper/lower. The lit run for
    # column i therefore falls on the OPPOSITE strip; `swap` exchanges the two.
    for i, (sx0, sx1) in enumerate(TRUE_SPANS):
        lit_upper = (i % 2 == 1) != swap
        lit_lower = (i % 2 == 0) != swap
        for (r0, r1), lit in ((STRIP_UPPER, lit_upper), (STRIP_LOWER, lit_lower)):
            if lit:
                img[r0:r1 + 1, sx0:sx1 + 1] = STRIP_LIT

    if dither:
        rng = np.random.default_rng(0)
        noise = rng.integers(-dither, dither + 1, size=img.shape, endpoint=False)
        img = np.clip(img.astype(np.int16) + noise, 0, 255).astype(np.uint8)
    return img


class StubDetector:
    """Minimal duck-typed stand-in for `MarkerBandDetector`.

    `build_calibration_from_markers` consumes exactly two methods; this pins that contract and
    lets a test feed the builder x-spans of its own choosing, independent of any CV.
    """

    def __init__(self, spans=None, strips=None):
        self.spans = list(spans if spans is not None else TRUE_SPANS)
        self.strips = list(strips if strips is not None else [STRIP_UPPER, STRIP_LOWER])

    def find_strips(self, frame_gray):
        return list(self.strips)

    def vial_boundaries(self, frame_gray):
        return list(self.spans)


@pytest.fixture
def detector() -> MarkerBandDetector:
    """A real detector with both synthetic faces registered."""
    det = MarkerBandDetector()
    det.register_face(make_face_frame(swap=False), "A")
    det.register_face(make_face_frame(swap=True), "B")
    return det


