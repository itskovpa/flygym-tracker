# =============================================================================================
# Tube row bands: found from the frame, and NOT confused by the brighter stage
# =============================================================================================
#: The row-mean profile is smoothed over `MarkerCalibParams.band_smooth` (9) rows before it is
#: thresholded, which pulls each run's edges in by about half a window. Band edges are therefore
#: compared with this tolerance rather than exactly.
BAND_TOL = 6


def assert_band_close(got, expected, tol=BAND_TOL):
    assert abs(got[0] - expected[0]) <= tol and abs(got[1] - expected[1]) <= tol, \
        f"band {got} not within {tol}px of {expected}"


def test_tube_row_bands_are_the_runs_adjacent_to_the_marker_band():
    gray = make_face_frame()
    upper, lower, central = tube_row_bands(gray, [STRIP_UPPER, STRIP_LOWER], X_EXTENT)

    assert_band_close(upper, UPPER_BAND)
    assert_band_close(lower, LOWER_BAND)
    assert central == (upper[1], lower[0])
    # the whole marker band lies inside the excluded central region, and neither tube row
    # touches it
    assert central[0] <= STRIP_UPPER[0] and STRIP_LOWER[1] < central[1]
    assert upper[1] <= STRIP_UPPER[0] and lower[0] > STRIP_LOWER[1]
    # each band still covers the bright tube core it was meant to find
    assert upper[0] <= UPPER_BAND[0] + EDGE_ROWS and upper[1] >= UPPER_BAND[1] - EDGE_ROWS
    assert lower[0] <= LOWER_BAND[0] + EDGE_ROWS and lower[1] >= LOWER_BAND[1] - EDGE_ROWS


def test_tube_row_bands_reject_the_brighter_stage():
    """The stage is brighter (255 vs 150) and tall enough to be a candidate run -- the lower
    tube row must still win, because it is the run NEAREST the band."""
    gray = make_face_frame()
    _, lower, _ = tube_row_bands(gray, [STRIP_UPPER, STRIP_LOWER], X_EXTENT)
    assert_band_close(lower, LOWER_BAND)
    assert lower[1] <= STAGE_ROW, "picked the saturated stage instead of the tube row"
    assert float(gray[STAGE_ROW:].mean()) > float(gray[lower[0]:lower[1]].mean())


def test_tube_row_bands_raise_when_a_side_is_missing():
    """With nothing lit below the marker band at all, there is no lower row to find."""
    gray = make_face_frame()
    gray[LOWER_BAND[0]:, :] = BG            # blank the lower tube row AND the stage
    with pytest.raises(ValueError, match="tube row on each side"):
        tube_row_bands(gray, [STRIP_UPPER, STRIP_LOWER], X_EXTENT)


